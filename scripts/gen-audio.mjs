// Generates MP3 audio for all vocabulary + kanji items via OpenAI TTS.
// Run: $env:OPENAI_API_KEY="sk-..."; node scripts/gen-audio.mjs
import { writeFileSync, mkdirSync, existsSync } from 'node:fs'

const KEY = process.env.OPENAI_API_KEY
if (!KEY) { console.error('Set OPENAI_API_KEY first'); process.exit(1) }
mkdirSync('public/audio', { recursive: true })

async function tts(id, text) {
  const file = `public/audio/${id}.mp3`
  if (existsSync(file)) { process.stdout.write('.'); return }
  const res = await fetch('https://api.openai.com/v1/audio/speech', {
    method: 'POST',
    headers: { Authorization: `Bearer ${KEY}`, 'Content-Type': 'application/json' },
    body: JSON.stringify({ model: 'tts-1', voice: 'nova', input: text, response_format: 'mp3' }),
  })
  if (!res.ok) { console.error(`\nERR ${res.status} "${text}"`); return }
  writeFileSync(file, Buffer.from(await res.arrayBuffer()))
  process.stdout.write('✓')
  await new Promise(r => setTimeout(r, 260))
}

